public class Smartphones {
    private String brand;
    private String model;
    private String operationSystem;
    private double screenSize;
    private String screenResolution;
    private int buildInMemoryCapacity;
    private boolean sdCardSupport;
    private int batteryCapacity;
    private int mainCameraMPixels;


    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getOperationSystem() {
        return operationSystem;
    }

    public void setOperationSystem(String operationSystem) {
        this.operationSystem = operationSystem;
    }

    public double getScreenSize() {
        return screenSize;
    }

    public void setScreenSize(double screenSize) {
        this.screenSize = screenSize;
    }

    public String getScreenResolution() {
        return screenResolution;
    }

    public void setScreenResolution(String screenResolution) {
        this.screenResolution = screenResolution;
    }

    public int getBuildInMemoryCapacity() {
        return buildInMemoryCapacity;
    }

    public void setBuildInMemoryCapacity(int buildInMemoryCapacity) {
        this.buildInMemoryCapacity = buildInMemoryCapacity;
    }

    public boolean isSdCardSupport() {
        return sdCardSupport;
    }

    public void setSdCardSupport(boolean sdCardSupport) {
        this.sdCardSupport = sdCardSupport;
    }

    public int getBatteryCapacity() {
        return batteryCapacity;
    }

    public void setBatteryCapacity(int batteryCapacity) {
        this.batteryCapacity = batteryCapacity;
    }

    public int getMainCameraMPixels() {
        return mainCameraMPixels;
    }

    public void setMainCameraMPixels(int mainCameraMPixels) {
        this.mainCameraMPixels = mainCameraMPixels;
    }
}
